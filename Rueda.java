package ejercicio1;

/**
 *
 * @author Estudiante
 */
public class Rueda {
    private int tamaño;
    private String marca;

    public Rueda(int tamaño, String marca) {
        this.tamaño = tamaño;
        this.marca = marca;
    }

    public int getTamaño() {
        return tamaño;
    }

    public void setTamaño(int tamaño) {
        this.tamaño = tamaño;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    @Override
    public String toString() {
        return " de tamaño" + tamaño + " y de marca: " + marca;
    }
    
}
